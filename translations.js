// Language configuration
const i18nConfig = {
    defaultLanguage: 'tr',
    fallbackLanguage: 'en',
    supportedLanguages: ['tr', 'en', 'de'],
    languageNames: {
        tr: 'Türkçe',
        en: 'English', 
        de: 'Deutsch'
    }
};

// Detect browser language
function detectBrowserLanguage() {
    // Get browser language (first 2 characters)
    const browserLang = (navigator.language || navigator.userLanguage || 'en').substring(0, 2).toLowerCase();
    
    // Check if browser language is supported
    if (i18nConfig.supportedLanguages.includes(browserLang)) {
        return browserLang;
    }
    
    // Fallback to default
    return i18nConfig.defaultLanguage;
}

// Current language state
let currentLanguage = localStorage.getItem('language') || detectBrowserLanguage();

// Translation function
function t(key) {
    const translation = translations[currentLanguage]?.[key] || 
                       translations[i18nConfig.fallbackLanguage]?.[key] || 
                       key;
    return translation;
}

// Get available languages
function getAvailableLanguages() {
    return i18nConfig.supportedLanguages.map(lang => ({
        code: lang,
        name: i18nConfig.languageNames[lang]
    }));
}

// Set language
function setLanguage(lang) {
    if (i18nConfig.supportedLanguages.includes(lang)) {
        currentLanguage = lang;
        localStorage.setItem('language', lang);
        updatePageContent();
        document.documentElement.lang = lang;
        
        // Update language switcher display
        if (typeof updateCurrentLanguageDisplay === 'function') {
            updateCurrentLanguageDisplay();
        }
    }
}

// Get current language
function getCurrentLanguage() {
    return currentLanguage;
}